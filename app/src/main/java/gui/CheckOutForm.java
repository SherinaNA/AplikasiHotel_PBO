    private void btnCheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckOutActionPerformed
        String selectedId = (String) cmbNoId.getSelectedItem();
        if (selectedId == null) {
            JOptionPane.showMessageDialog(this, "Please select a reservation ID.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Reservation reservation = reservationDAO.get(Integer.parseInt(selectedId));
        if (reservation == null) {
            JOptionPane.showMessageDialog(this, "Reservation not found.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String checkOutDate = txtTglOut.getText();
        
        // Mengupdate data checkout dengan date dan menghitung otomatis total price
        reservationDAO.checkOut(Integer.parseInt(selectedId), checkOutDate);

        JOptionPane.showMessageDialog(this, "Check out successful.", "Success", JOptionPane.INFORMATION_MESSAGE);

        // Refresh data in MainPage
        mainPage.refreshData();
        dispose();
    }//GEN-LAST:event_btnCheckOutActionPerformed

