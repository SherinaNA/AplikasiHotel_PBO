    public void tampil_noId(){
        noIdList = reservationDAO.getId();
        cmbNoId.removeAllItems();
        for (String id : noIdList) {
            cmbNoId.addItem(id);
        }
    }
    
    public void onIdSelected(){
        String selectedId = (String) cmbNoId.getSelectedItem();
        if (selectedId != null) {
            Reservation reservation = reservationDAO.get(Integer.parseInt(selectedId));
            Tamu tamu = tamuDAO.getNoKtp(reservation.getNoKtp());
            Kamar kamar = kamarDAO.get(reservation.getNoKamar());

            lblKtp.setText(tamu.getNoKtp());
            lblTamu.setText(tamu.getNama());
            lblKamar.setText(String.valueOf(kamar.getNoKamar()));
            lblCheckInDate.setText(reservation.getCheckInDate());
            lblDepo.setText(String.valueOf(reservation.getDeposit()));
        }
    }

